#Methods of lists
list = [1, 8, 7, 2, 21, 15]
print(list)
list.sort()                      # sorts the list
print(list)
list.reverse()                   # reverses the list
print(list)
list.append(45)                  # adds 45 at the end of the list
print(list)
list.insert(2, 544)              # inserts 544 at index 2
print(list)
list.pop(2)                      # removes element at index 2
print(list)
list.remove(21)                  # removes 21 from the list
print(list)