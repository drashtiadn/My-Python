#String Function
a = "hello world, nice to meet you all.hope to see you soon"
print(len(a))      #Length
print(a.endswith("fish"))    #endswith
print(a.count("l"))          #counts the letter or words
print(a.capitalize())        #capitalize first letter of the string
print(a.find("to"))          #first occurance of the word
print(a.replace("nice","good"))  #replace(oldword,newword)
print(a.split())              #Split a string by blank space (this is the default)

